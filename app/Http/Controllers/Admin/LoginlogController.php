    public function data(Request $request)
    {
        $data = $request->all('username');
        $list = LoginLog::query()->when($data['username'], function ($query, $data) {
            return $query->where('username', 'like', '%' . $data . '%');
        })->paginate($request->get('limit', 30));

        $data = [
            'code' => 0,
            'msg' => '请求中',
            'count' => $list->total(),
            'data' => $list->items()
        ];

        return response()->json($data);
    }

